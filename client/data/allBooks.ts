export const oldTestamentBooks = allBooks.filter(
  (book) => book.testament === "old"
);
export const newTestamentBooks = allBooks.filter(
  (book) => book.testament === "new"
);

export const getBookById = (id: string) => allBooks.find((book) => book.id === id);

export const searchBooks = (query: string, testament?: "old" | "new") => {
  const searchTerm = query.toLowerCase();
  const booksToSearch = testament
    ? testament === "old"
      ? oldTestamentBooks
      : newTestamentBooks
    : allBooks;

  return booksToSearch.filter(
    (book) =>
      book.title.toLowerCase().includes(searchTerm) ||
      book.titleEnglish.toLowerCase().includes(searchTerm) ||
      book.category.toLowerCase().includes(searchTerm) ||
      book.summary.toLowerCase().includes(searchTerm)
  );
};

export const getCategories = (testament?: "old" | "new") => {
  const books = testament
    ? testament === "old"
      ? oldTestamentBooks
      : newTestamentBooks
    : allBooks;
  const categories = [...new Set(books.map((book) => book.category))];
  return categories;
};

export const getAllBooksCount = () => allBooks.length;
export const getOldTestamentCount = () => oldTestamentBooks.length;
export const getNewTestamentCount = () => newTestamentBooks.length;
